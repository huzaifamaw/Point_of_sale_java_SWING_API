    private boolean checkforname(String Name){
            DefaultTableModel model = (DefaultTableModel) tblItemsOrignal.getModel();
            for (int j = 0; j < model.getRowCount(); j++) {
            if(tblItemsOrignal.getValueAt(j, 1).equals(Name)){
                return true;
            }
        }
            return false;
    }
    
