    private void btnItemDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnItemDeleteActionPerformed
if(txtItemRate.getText().equals("")||txtItemItem.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Fields Cannot be empty");}else{            
        
        int a = JOptionPane.showConfirmDialog(null,"Are you sure?");
            if(a==0){
                boolean b = s.deleteAllItemsIndeals(Integer.parseInt(txtItemsID.getText()));
                
                        if(b){
                            boolean c = s.deleteDeal(Integer.parseInt(txtItemsID.getText()));
                            if(c){
                                DefaultTableModel model = (DefaultTableModel) tblItemsOrignal.getModel();
                                model.setRowCount(0);
                                model = (DefaultTableModel) tblItems.getModel();
                                for (int j = 0; j < tblItems.getRowCount(); j++) {
                                    if(Integer.parseInt(model.getValueAt(j, 0).toString())==Integer.parseInt(txtItemsID.getText())){
                                        model.removeRow(j);
                                        break;
                                    }
                                }
                                txtItemItem.setText("");
                                txtItemRate.setText("");
                                txtItemsID.setText("");
                                combolistcatagory.setVisible(false);
                                jLabel6.setVisible(false);
                            }
                        }
            }}
        
    }//GEN-LAST:event_btnItemDeleteActionPerformed

